package org.neo4j.server.extension.statistic;

import javax.ws.rs.GET;
import javax.ws.rs.Path;
import javax.ws.rs.QueryParam;
import javax.ws.rs.core.Context;
import javax.ws.rs.core.Response;
import java.io.IOException;

@Path("/")
public class StatisticsResource {

    private final StatisticCollector statistics;

    public StatisticsResource(@Context StatisticCollector statistics) {
        this.statistics = statistics;
    }

    @GET
    public Response addUserRo(@QueryParam("clear") Long clear) throws IOException {
        if (clear != null) {
            statistics.purgeStatistics(clear);
        }
        return statistics.createResponse();
    }
}